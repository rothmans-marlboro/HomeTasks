package HomeTask2;

public class TaskAddNumber3 {
	/*
	 * The program which eliminates the squares of the first ten numbers
	 */
	public static void main(String[] args) {
		int n = 1;
		while (n <= 10) {
			System.out.println("Meaning n=" + n + ";" + " square meaning=" + n * n);
			n++;
		}
	}
}